type WidenLeaves<T> = T extends string
  ? string
  : {
      readonly [K in keyof T]: WidenLeaves<T[K]>;
    };

type Join<K, P> = K extends string
  ? P extends string
    ? `${K}.${P}`
    : never
  : never;

type LeafPaths<T> = T extends string
  ? never
  : {
      [K in keyof T]: T[K] extends string ? K & string : Join<K, LeafPaths<T[K]>>;
    }[keyof T];

